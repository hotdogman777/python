# folium 지도 생성
m = folium.Map(location=[35.5, 128.7], zoom_start=8, tiles='cartodbpositron')

# 도시별 마커 추가
for name, lat, lon in cities:
    folium.Marker(
        location=[lat, lon],
        popup=f"<b>{name}</b><br>강우량 입력: <input type='text' placeholder='mm 단위'>",
        tooltip=name,
        icon=folium.Icon(color='blue', icon='cloud')
    ).add_to(m)

# HTML 저장
m.save("rain_input_map.html")
